'use strict';

const PAYMENT_PURPOSES = Object.freeze({
    WALLET_TOPUP: 'WALLET_TOPUP',
});

const PAYMENT_GATEWAYS = Object.freeze({
    MOCK: 'MOCK',
    NETWORK_INTERNATIONAL: 'NETWORK_INTERNATIONAL',
    PAYMENTO: 'PAYMENTO',
    ZIINA: 'ZIINA',
    TAP: 'TAP',
});

const PAYMENT_METHODS = Object.freeze({
    CARD: 'CARD',
    ONLINE: 'ONLINE',
});

const PAYMENT_STATUSES = Object.freeze({
    INITIATED: 'INITIATED',
    PENDING: 'PENDING',
    REQUIRES_ACTION: 'REQUIRES_ACTION',
    SUCCEEDED: 'SUCCEEDED',
    FAILED: 'FAILED',
    CANCELED: 'CANCELED',
    EXPIRED: 'EXPIRED',
});

const ACTIVE_PAYMENT_STATUSES = Object.freeze([
    PAYMENT_STATUSES.INITIATED,
    PAYMENT_STATUSES.PENDING,
    PAYMENT_STATUSES.REQUIRES_ACTION,
]);

const TERMINAL_PAYMENT_STATUSES = Object.freeze([
    PAYMENT_STATUSES.SUCCEEDED,
    PAYMENT_STATUSES.FAILED,
    PAYMENT_STATUSES.CANCELED,
    PAYMENT_STATUSES.EXPIRED,
]);

const PAYMENT_TRANSITIONS = Object.freeze({
    [PAYMENT_STATUSES.INITIATED]: [
        PAYMENT_STATUSES.PENDING,
        PAYMENT_STATUSES.REQUIRES_ACTION,
        PAYMENT_STATUSES.SUCCEEDED,
        PAYMENT_STATUSES.FAILED,
        PAYMENT_STATUSES.CANCELED,
        PAYMENT_STATUSES.EXPIRED,
    ],
    [PAYMENT_STATUSES.PENDING]: [
        PAYMENT_STATUSES.REQUIRES_ACTION,
        PAYMENT_STATUSES.SUCCEEDED,
        PAYMENT_STATUSES.FAILED,
        PAYMENT_STATUSES.CANCELED,
        PAYMENT_STATUSES.EXPIRED,
    ],
    [PAYMENT_STATUSES.REQUIRES_ACTION]: [
        PAYMENT_STATUSES.PENDING,
        PAYMENT_STATUSES.SUCCEEDED,
        PAYMENT_STATUSES.FAILED,
        PAYMENT_STATUSES.CANCELED,
        PAYMENT_STATUSES.EXPIRED,
    ],
    [PAYMENT_STATUSES.SUCCEEDED]: [],
    [PAYMENT_STATUSES.FAILED]: [],
    [PAYMENT_STATUSES.CANCELED]: [],
    [PAYMENT_STATUSES.EXPIRED]: [],
});

const isAllowedPaymentTransition = (fromStatus, toStatus) => {
    if (fromStatus === toStatus) return true;
    return (PAYMENT_TRANSITIONS[fromStatus] || []).includes(toStatus);
};

module.exports = {
    PAYMENT_PURPOSES,
    PAYMENT_GATEWAYS,
    PAYMENT_METHODS,
    PAYMENT_STATUSES,
    ACTIVE_PAYMENT_STATUSES,
    TERMINAL_PAYMENT_STATUSES,
    PAYMENT_TRANSITIONS,
    isAllowedPaymentTransition,
};
